#include "../global.h"

int	map_includes(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == '1' || str[i] == '0' || str[i] == 'C'
			|| str[i] == 'E' || str[i] == 'P' || str[i] == 'X')
		{
			i++;
			continue ;
		}
		return (1);
	}
	return (0);
}
